class binary_operator2 {
    
    
        public static void main(String[] args) {
            int a=6,b=2;
            System.out.println(a>b);//6>2t
            System.out.println(a<b);//6<2f
            System.out.println(a>=b);//6>=2t
            System.out.println(a<=b);//6<=2f
            System.out.println(a==b);//6==2f
            System.out.println(a!=b);//6!=2t
        }
    }
    

